let num = prompt("Введите число от 0 до 5: ");

switch(num) {
    case "0":
    console.log("Ноль");
    break;
    case "1":
    console.log("Один");
    break;
    case "2":
    console.log("Два");
    break;
    case "3":
    console.log("Три");
    break;
    case "4":
    console.log("Четыре");
    break;
    case "5":
    console.log("Пять");
    break;
    default:
    console.log("Вы ввели некорректное число");
}
